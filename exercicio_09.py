# 9 - Faça um programa que converta a temperatura de Celsius para Fahrenheit.

graus_celsius = float(input("Digite os graus celsius para fazer a conversão: "))

fahrenheit = float((9 * graus_celsius) / 5) + 32


print(f"Portanto, {graus_celsius} graus Celsius equivalem a {fahrenheit} graus Fahrenheit")