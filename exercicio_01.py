# Inteiros (int)

# 1 - Escreva um programa que soma dois números inteiros inseridos pelo usuário.
soma_valor_1 = int(input("Digite um número: "))
soma_valor_2 = int(input("Digite outro número: "))

valor_total_soma = soma_valor_1 + soma_valor_2

print("Esse é o valor total da soma", valor_total_soma)